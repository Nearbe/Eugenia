#!/usr/bin/env python3
"""
Проверка производительности цикла самообращений 1→0→1.

Назначение
==========
Измеряет скорость вызовов самообращения в CPython:
сколько кадров вызовов создаётся и освобождается за секунду.

Алгоритм
========
1. Устанавливает recursionlimit = 20 000 000 (по умолч. 1 000).
2. Запускает бесконечный цикл по шагам 0..9, 0..9, ...
3. На каждом шаге: f(5_000_000) — 5М вложенных вызовов самообращения.
4. Измеряет время через time.perf_counter().
5. Выводит: скорость в вызовах/сек.

Поведение на уровне CPython и техники
==========================================

1. Выделение кадров вызовов (PyFrameObject)
---------------------------------------------
   Каждый вызов ``f(n)`` создаёт объект ``PyFrameObject``:
   - Локальные переменные
   - Стек операндов
   - Указатель на текущую инструкцию (lasti)
   - Ссылка на объект кода
   - Ссылка на globals/locals

   CPython НЕ использует ``malloc()`` для каждого фрейма. PyObject_Malloc
   кэширует малые объекты (< 512 байт). После первого выделения создаются
   внутренние пулы. Повторное выделение — взятие из кэша (~5–10 нс).
   После завершения: frame_clear → возврат в кэш.

   КРИТИЧНО: КАЖДАЯ функция самообращения требует СВОЙ кадр на C-стеке.
   Все 5 миллионов кадров существуют ОДНОВРЕМЕННО при разворачивании.

2. ``sys.setrecursionlimit()``
--------------------------
   Устанавливает ``recursion_limit`` в ``PyInterpreterState``.
   Проверка в ceval.c при каждом входе в функцию:
   - Если глубина >= recursion_limit → RecursionError.
   - По умолчанию: 1000. Мы ставим 20 000 000, чтобы f(5_000_000) не вызвала RecursionError.
   - НЕ изменяет размер OS-стека (по умолчанию на macOS — 8 МБ на поток).
   - При 5М вызовов: ~128–256 байт/кадр × 5М ≈ 640 МБ – 1,2 ГБ C-стека.
   - Если OS-стек меньше → segfault (переполнение стека), НЕ RecursionError.

3. Функция самообращения ``f(n)``
-------------------------------
   Байткод (python3 -m dis):
       LOAD_FAST    'n'          # Загрузить n в стек
       LOAD_CONST   0            # Загрузить 0
       COMPARE_OP   <=           # Проверить n <= 0
       POP_JUMP_IF_FALSE  <label> # Если False — перейти к самообращению
       LOAD_CONST   0            # Результат: 0
       RETURN_VALUE              # Вернуть
   <label>:
       LOAD_FAST    'n'          # Загрузить n
       LOAD_CONST   1            # Загрузить 1
       BINARY_SUBTRACT           # n - 1
       LOAD_GLOBAL  'f'          # Загрузить f
       CALL_FUNCTION  1          # Вызвать f(n-1) (~200–400 циклов CPU)
       LOAD_CONST   1            # Загрузить 1
       BINARY_ADD                # результат + 1
       RETURN_VALUE              # Вернуть

   Фаза погружения (descent):
       f(5_000_000) → f(4_999_999) → ... → f(0)

   Фаза разворачивания (unwind):
       f(0) → 0, f(1) = 0+1 → 1, f(2) = 1+1 → 2, ... f(5_000_000) → 5_000_001

   Важно: CPython НЕ оптимизирует хвостовые вызовы. Все 5 000 001 кадров
   существуют ОДНОВРЕМЕННО. Объём: ~200 байт × 5М ≈ 1 ГБ (PyFrameObject).
   Функция возвращает n + 1.

4. ``itertools.cycle(range(10))``
------------------------------
   C-уровневый итератор (тип ``_itertools.cycleobject``):
   - Малый список [0, 1, ..., 9] + целочисленный индекс.
   - ``__next__()`` в C — нулевые накладные расходы на байткод Python.
   - Возвращает list[index], увеличивает index % 10. ~50–100 тактов CPU.
   - Малые int (-5 до 256) кэшируются, уже в L1-кэше.
   - Преимущества перед ``while True``: нет LOAD_GLOBAL/COMPARE_OP/POP_JUMP.

5. ``time.perf_counter()``
--------------------------------
   На macOS: ``mach_absolute_time()`` — аппаратный TSC-счётчик через libSystem.
   Разрешение: ~1–5 нс. Монотонный (не зависит от системного времени).

   Что входит в (t2 - t1):
   - Выделение PyFrameObject в кэш: ~5–10 нс/кадр (по мере необходимости)
   - Байткод (LOAD_FAST, BINARY_SUBTRACT): ~8–12 нс/инструкция
   - CALL_FUNCTION диспетчеризация: ~200–400 циклов CPU (~50–100 нс)
   - frame_clear → возврат в кэш: ~5–10 нс/кадр
   - BINARY_ADD (+1): ~2–3 нс (ALU, 1 такт)
   - print → очистка буфера stdout: ~100–500 мкс (I/O, переменный)

6. Что на самом деле измеряет бенчмарк
   - Пропускная способность CPython интерпретатора: сколько фреймов можно добавить +
     удалить в секунду на этом оборудовании.
   - Пропускная способность памяти: 5 М × ~200 байт = ~1 ГБ стека за итерацию,
     в основном ограничено пропускной способностью памяти.
   - Пропускная способность целочисдений: каждый путь возврата делает ``result + 1`` —
     тривиальная операция ALU (одноцикловая), но инструкция ADD находится на критическом
     пути.

7. Поведение физического оборудования (M4 Max, 12P+4E ядер)
   - Все 5 М вызовов самообращения выполняются в одном потоке (главный GIL поток).
   - P-ядра обрабатывают горячий цикл; E-ядра простаивают.
   - ЦПУ частота: ~1.5–3.2 ГГц (ускорение). Ожидаемая пропускная способность: ~25 М
     вызовов/сек, согласованная с пропускной способностью ЦПУ, ограниченной предсказанием
     ветвей и пропускной способностью ALU (а не пропускной способностью памяти).

Использование
=====
    python3 bench_recursive.py # Запустить для всех 10 шагов (0–9, повторять)
    python3 bench_recursive.py 2>&1 | tee output.log # Захватить вывод

Формат вывода
=============
    Шаг N: 5000001 вызовов за X.XXX с = Y, ZZZ, YYY вызовов/с

    Где:
      N — итерация цикла (0–9, бесконечно повторяется)
      5000001 — общее число вызовов включая начальный f(5_000_000)
      X.XXX — время в секундах (точность perf_counter ~нс)
      Y,ZZZ — сырая пропускная способность в вызовах/сек

"""

import itertools  # C-уровневый итератор: cycle(range(10)) → 0,1,...,9,0,1,...
import sys  # sys.setrecursionlimit — управление защитой стека CPython
import time  # time.perf_counter — высокоточный таймер на основе TSC процессора


def f(n: int) -> int:
    """Функция самообращения тождества: f(n) = n.

    Поведение на физическом уровне
    ------------------------
    Размещает ровно ``n`` кадров на стеке CPython перед разворачиванием.

    Цепочка вызовов:
        f(5_000_000)
          └─> f(4_999_999)
                └─> f(4_999_998)
                      ...
                            └─> f(0)   ← базовый случай, возвращает 0 немедленно

    Цепочка разворачивания (возврат всплывает):
        f(0) → возвращает 0
        f(1) = f(0) + 1 → возвращает 1
        f(2) = f(1) + 1 → возвращает 2
        ...
        f(5_000_000) = f(4_999_999) + 1 → возвращает 5_000_001

    Общее число вызовов: n + 1 (начальный вызов плюс n вызовов самообращения).
    Каждый вызов выделяет кадр (~200 байт), значит 5 М кадров ≈ 1 ГБ живого стека.

    Функция НЕ использует оптимизацию хвостовых вызовов — в CPython их нет.
    Каждый кадр остаётся на стеке до тех пор, пока его вызывающая функция не вернётся,
    то есть все 5 М кадров сосуществуют в памяти одновременно во время фазы разворачивания.

    Возвращает: int — ровно ``n + 1`` (число размещённых кадров, включая корневой вызов).
    """
    if n <= 0:  # Базовый случай: n == 0 (или отрицательный, хотя сюда никогда не попадём)
        return 0  # Возвращает немедленно — дальнейший рост стека не требуется
    return f(n - 1) + 1  # Размещает кадр, затем добавляет 1 на пути возврата


def main() -> None:
    # ── Расширение лимита стека ──────────────────────────────────────────────
    # recursion_limit CPython — это защитный механизм в ceval.c.
    # По умолчанию: 1000 кадров → RecursionError на глубине ~1000.
    # Устанавливаем 20 М, чтобы f(5_000_000) НЕ вызвал RecursionError.
    # Это программная проверка — он НЕ изменяет размер OS-стека.
    sys.setrecursionlimit(2_147_483_647)

    # ── Бесконечный цикл 0..9 ─────────────────────────────────────────
    # itertools.cycle — C-уровневый итератор (_itertools.cycleobject).
    # __next__() реализован в C — нулевые накладные расходы на байткод Python.
    # Внутри: хранит малый список [0..9] и индекс; возвращает list[index],
    # увеличивает индекс по модулю len(list). ~50–100 тактов на вызов __next__().
    for step in itertools.cycle(range(10)):
        # ── Настройка итерации ────────────────────────────────────────
        total = 20  # Число вызовов самообращения для размещения (каждый кадр ~200 байт)

        # ── Точка отсчёта: до вызова ─────────────────────────────────────────
        # time.perf_counter() на macOS использует mach_absolute_time() — чтение TSC процессора.
        # Разрешение: ~1–5 нс. Монотонный (никогда не идёт назад). Не зависит от
        # изменений системного времени. Базовый системный вызов: MACH_ABSOLUTE_TIME.
        start = time.perf_counter()

        # ── Вызов самообращения ────────────────────────────────────────
        # f(total) размещает 5 М кадров (≈1 ГБ стека), затем разворачивает их.
        # Разворачивание: каждый кадр делает BINARY_ADD (+1) + RETURN_VALUE.
        # Общая работа: 5 М размещений кадров + 5 М освобождений кадров + 5 М целочисленных сложений.
        count = f(total)

        # ── Точка отсчёта: после вызова ────────────────────────────────────────
        elapsed = time.perf_counter() - start  # Длительность в секундах

        # ── Расчёт пропускной способности ─────────────────────────────────────
        # calls_per_sec = total / elapsed → кадры размещённые+освобождённые в секунду.
        # На M4 Max: ожидаем 40–80 М вызовов/сек, ограничено пропускной способностью памяти
        # (1 ГБ стека за итерацию) и предсказанием ветвей (линейное самообращение).
        calls_per_sec = total / elapsed

        # ── Вывод ─────────────────────────────────────────────────────
        print(f"Шаг {step}: {count} вызовов за {elapsed:.9f}с = {calls_per_sec:,.9f} вызовов/с")


if __name__ == "__main__":
    main()
