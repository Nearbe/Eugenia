#!/usr/bin/env python3

from __future__ import annotations

from itertools import cycle
from sys import set_int_max_str_digits, setrecursionlimit, setswitchinterval
from time import perf_counter, process_time


# ── The pure cycle ───────────────────────────────────────────────────────────
# Эта часть кода не должна изменяться.
# Она олицетворяет бесконечность через вечность.
# Или же пустота, что по-сути является 0 или точкой начала Вселенной.
# Её присутствие в коде это буквально ручка которая дотягивается то старта.
# Когда-то вся Вселенная выглядела именно так.
def f() -> None:
    return


# ── Core computation ─────────────────────────────────────────────────────────
# Самое прекрасное место это место между микро и макро.
# Место куда можно пихать код бесконечно и он будет бесконечно исполнятся.
# Мы не ограничены в количестве кода который можем сюда запихнуть.
# Мы не ограничены в скорости исполнения кода который сюда запихнут.
# Вопрос в том, как с практической точки зрения подойти к решению этого вопроса.
# Чтобы просто не перегружать 1 файл символами, это бессмысленно.
def main() -> None:
    count = 0
    wall_start = perf_counter()
    cpu_start = process_time()

    for step in cycle(range(5)):
        count += 1
        print(f"{step}, {count}")
        f()

    wall_elapsed = perf_counter() - wall_start
    cpu_elapsed = process_time() - cpu_start

    # ── Итоги ────────────────────────────────────────────────────────────────

    print("\n--- Итоги ---")
    print(f"Итераций:       {count}")
    print(f"Wall clock:     {wall_elapsed:.6f} секунд")
    print(f"CPU time:       {cpu_elapsed:.6f} секунд")
    print(f"Итер/сек (wall):{count / wall_elapsed:.0f}")
    print(f"Итер/сек (cpu): {count / cpu_elapsed:.0f}")


# ── Runtime settings ─────────────────────────────────────────────────────────
# Не до конца понятная область кода.
# По идеи это точка запуска программы.
# А где запускается эта программа?
# Допустим через terminal с помощью команды.
# Это конечно же не единственный способ запустить программу.
# Это точка 0, там где программа теряет свою суть.
# Для всего что с ней взаимодействует она становиться ничем, по-сути нулём.
# Поэтому скрипт на питоне являясь программой не привязан к способу запуска.
if __name__ == "__main__":
    # Не до конца понятно зачем
    # Но чувствую что так надо.
    # Вообще идея в том, чтобы убрать всё,
    # что может исскуственно ограничить исполнение.
    set_int_max_str_digits(0)

    # Пока не до конца понятно почему именно так.
    # В определенном смысле поведение не меняется.
    # Нужно выяснить условия при которых меняется.
    # Неправильная цифра установленная тут может убить процессор.
    # Создав замыкание не на ту линию.
    # Хотя для цикла из 10 шагов внутри main,
    # стабильность сохраняется при любой цифре.
    setswitchinterval(0.00000000000000001)

    # 3 потому что у нас 3 сущности в памяти в момент исполнения.
    # Это не минимум, но не максимум.
    # Нужно просто понять как этим управлять.
    setrecursionlimit(3)

    # Эта точка, где проходит черта.
    # Это точка где 0 распадается на 10 шагов.
    # По-факту это ложь. Потому что перед вложенным циклом есть еще одна область исполнения.
    # То есть любой код за этой точкой может быть исполнен только в строго определенном порядке.
    # Область исполнения: тело функции main до начала цикла.
    main()
