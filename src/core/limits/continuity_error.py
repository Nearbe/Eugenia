def continuity_error(fn, sequence: list[float], x_limit: float) -> float:
    """Return |f(lim xₙ) - f(x_tail)|."""
    if not sequence:
        return 0.0
    y_limit = fn(x_limit)
    y_actual = fn(sequence[-1])
    return abs(float(y_limit) - float(y_actual))
