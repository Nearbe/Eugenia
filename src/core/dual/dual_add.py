from ..states.dual_number import dual_number


def dual_add(x1, v1, x2, v2):
    """Return components of ``(x₁+v₁ε) ⊕ (x₂+v₂ε)``."""
    return dual_number(x1, v1).add(dual_number(x2, v2)).as_tuple()
